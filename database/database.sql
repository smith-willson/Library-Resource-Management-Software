-- TABLE CREATION

CREATE TABLE Users (
    userID INTEGER PRIMARY KEY,
    username TEXT,
    password TEXT,
    firstName TEXT,
    lastName TEXT,
    address TEXT,
    balance REAL
);

CREATE TABLE Students (
    userID INTEGER PRIMARY KEY,
    rollNo INTEGER,
    department TEXT,
    batch INTEGER,
    FOREIGN KEY (userID) REFERENCES Users(userID)
);

CREATE TABLE Teachers (
    userID INTEGER PRIMARY KEY,
    employeeID INTEGER,
    department TEXT,
    designation TEXT,
    FOREIGN KEY (userID) REFERENCES Users(userID)
);

CREATE TABLE Admins (
    userID INTEGER PRIMARY KEY,
    adminID INTEGER,
    FOREIGN KEY (userID) REFERENCES Users(userID)
);

CREATE TABLE Resources (
    resourceID INTEGER PRIMARY KEY,
    title TEXT,
    author TEXT,
    copiesAvailable INTEGER
);

CREATE TABLE BorrowRecords (
    recordID INTEGER PRIMARY KEY,
    userID INTEGER,
    resourceID INTEGER,
    borrowDate TEXT,
    dueDate TEXT,
    returnDate TEXT,
    fine REAL,
    FOREIGN KEY (userID) REFERENCES Users(userID),
    FOREIGN KEY (resourceID) REFERENCES Resources(resourceID)
);

